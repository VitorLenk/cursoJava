public class Conversor
{
    public static void main(String[] args)
    {

        final double divisao = 5.0 / 9.0;
        final double subt = 32;
        double variavel = 86;
        double formula = (variavel - subt) * divisao;

        System.out.println("Conversão = " + formula + " Celsius.");


        variavel = 100;
        formula = (variavel - subt) * divisao;

        System.out.println("Conversão = " + formula + " Celsius.");


    }

}
